import os, re
base = r'c:\Users\Dell\Desktop\claude 1\dental-clinic\frontend\src\pages'

def process(file, reps):
    path = os.path.join(base, file)
    with open(path, 'r', encoding='utf-8') as f: content = f.read()

    if 'useLanguage' not in content:
        if file == 'Settings.jsx':
            content = content.replace('import { getSettings', 'import { useLanguage } from \'../LanguageContext\';\nimport { getSettings')
        else:
            content = content.replace('import { useParams', 'import { useLanguage } from \'../LanguageContext\';\nimport { useParams')
        content = re.sub(r'(export default function \w+\([^)]*\) {\n)', r'\1  const { t } = useLanguage();\n', content)

    for o, n in reps: content = content.replace(o, n)
    with open(path, 'w', encoding='utf-8') as f: f.write(content)

